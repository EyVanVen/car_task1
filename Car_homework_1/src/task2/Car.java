package task2;

public class Car {
    int year;
    String color;

    public Car (){
        this.year = 2000;
        this.color = "red";

    }

    public Car (int year){
        this.year = year;
        this.color = "green";
    }

    public Car (int year, String color){
        this.year = year;
        this.color = color;
    }


}
